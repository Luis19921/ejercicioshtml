public class Funciones {
    // Función que toma dos números enteros y devuelve su suma
    public static int sumar(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        // Llamada a la función sumar con dos números enteros
        int resultado = sumar(20, 20);
        
        // Muestra el resultado en la consola
        System.out.println("La suma de 20 y 20 es: " + resultado);
    }
}

